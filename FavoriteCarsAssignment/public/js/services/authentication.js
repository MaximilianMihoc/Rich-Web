/* Authentication service. 
	Used for registration, login, login with google and login with github. 

*/
mainManager.factory('Authentication', ['$rootScope', '$firebaseAuth', '$firebaseObject', '$location', 'FIREBASE_URL',
function($rootScope, $firebaseAuth, $firebaseObject, $location, FIREBASE_URL) {
	
	var ref = new Firebase(FIREBASE_URL);
    var auth = $firebaseAuth(ref);

    /* the next function is used to set the scope variables for user details used later in User profile view.*/
    auth.$onAuth(function(authData) {
     
    	if(authData){
    		var userRef = new Firebase(FIREBASE_URL + "users/" + authData.uid)
    		var userObj = $firebaseObject(userRef);

    		$rootScope.auth = authData;
    		$rootScope.currentUser = userObj;
    	}
    	else
    	{
    		$rootScope.auth = '';
    		$rootScope.currentUser = '';
    	}

    });
	
	/*	The next 4 functions are used for registration and authentication. */
	return {
		login: function(user) {
	      auth.$authWithPassword({
	        email    : (user.inputEmail).trim(),
	        password : user.inputPassword
	      }).then(function(authData) {
	          console.log("Authenticated successfully with payload:", authData);
	          //after login, redirect user to profile view and set the current path variable so that the navigation bar will be shown.
	          $location.path('/profile/' + authData.uid );
	          $rootScope.currentPath = $location.path();
	      }).catch(function(error){
	          console.log("Login Failed!", error);
	          $rootScope.message = 'The specified credentials does not exist. Please Register or try again.';
	      });
	    }, // end login
	    register: function(user) {
	    	var self = this;
	      auth.$createUser({
	        email    : (user.inputEmail).trim(),
	        password : user.inputPassword
	      }).then(function(userData) {
	          console.log("Successfully created user account with uid:", userData);
	          
	          // calculate gravatar hash and get the gravatar image URL
	          var emailHash = get_gravatar((user.inputEmail).trim(), 200);

	          // after registration, store the user details in firebase under the users object. a new UID is automatically generated by firebase.
	          var regRef = new Firebase(FIREBASE_URL + "users")
	          .child(userData.uid).set({
	            date: Firebase.ServerValue.TIMESTAMP,
	            regUser: userData.uid,
	            username: user.Inputusername,
	            profilePictureURL: emailHash

	          }); //set

	        //log in user after registration 
	        var loginUser = {
	        	inputEmail 		: "" + user.inputEmail,
	        	inputPassword 	: "" + user.inputPassword
	        };

	        // after registration, use the username and password to authenticate the user.
	        self.login(loginUser);
	      }).catch(function(error){
	          console.log("Error creating user:", error);
	      });

	  	}, // register
	  	logout: function() {
	    	return auth.$unauth();
	    }, //logout
	    // require auth is used if the user tries to open a page without being authenticated.
	    requireAuth: function() {
	    	return auth.$requireAuth();
	    }, //require Authentication

	    gitHublogin: function() {
		    ref.authWithOAuthPopup("github", function(error, authData) {
	          if (error) {
	            console.log("Login Failed!", error);
	          } else {
	            console.log("Authenticated successfully with payload:", authData);
	            // when the user registered with google, some information are stored in firebase regarding the user. 
	            // usrname, profile picture and id are all stored in firebase.
	            var regRef = new Firebase(FIREBASE_URL + "users")
		          .child(authData.uid).set({
		            date: Firebase.ServerValue.TIMESTAMP,
		            regUser: authData.uid,
		            username: authData.github.username,
		            profilePictureURL: authData.github.profileImageURL

		          }); //set

		          $location.path('/profile/' + authData.uid );
		          $rootScope.currentPath = $location.path();
	          }
	        }, 
	        {
	          remember: "sessionOnly",
	          scope: "user,gist"
	        });

	    }, // github login

	    googleLogin: function() {
	    	ref.authWithOAuthPopup("google", function(error, authData) {
	          if (error) {
	            console.log("Login Failed!", error);
	          } else {
	            console.log("Authenticated successfully with payload:", authData);

	            // username, profilepicture and user id informations are saved in firebase when the user logs in with github.
	            var regRef = new Firebase(FIREBASE_URL + "users")
		          .child(authData.uid).set({
		            date: Firebase.ServerValue.TIMESTAMP,
		            regUser: authData.uid,
		            username: authData.google.displayName,
		            profilePictureURL: authData.google.profileImageURL

		          }); //set
		          $location.path('/profile/' + authData.uid);
		          $rootScope.currentPath = $location.path();
	          }
	        }, 
	        {
	          remember: "sessionOnly",
	          scope: "email"
	        });
	    } // google login

	} //return


}]); //factory